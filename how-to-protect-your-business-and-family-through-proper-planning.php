                         <br /> <br />
						 
						 
						<p>Ensure your business—and the income it provides your family—is protected if you’re no longer in the picture </p><br><br>
						<img src="How20to20protect20your20business20and20family20through20proper20planning.jpg" />
						<br><br>
						<p>Entering my deserted office building earlier this month, I ran into an old work out buddy of mine who is now an owner of a pretty successful and popular take-out restaurant. Although we could not give each other hugs (per social distancing directives), I was pleasantly surprised to see him. After a bit of small talk, I could see that he was concerned with something and asked him what it was. With a look of curiosity and deep thought, my friend responded that he’d been meaning to reach out to me to ask what, if anything, would happen to his restaurant if he were to get sick or die. Understanding this is not a topic you normally discuss with your friends, he said that he’d been thinking about things like succession and legacy planning for his business, as the current state of affairs naturally invites these thoughts.</p>
						<br /> <br /> 
				
						<p>Here’s the truth: although nobody wants to think or talk about it, considering what would happen to your business upon your death or in the event of your incapacity is one of the single-most important things you can do for your business.<br>To ensure your business—and the income it provides your family—is protected if you’re no longer in the picture, you’ll need to create and maintain an effective estate plan. Without a proper estate plan, your business could quickly fall apart and leave your family at risk.</p>
					<br><br>
						<p><b>Where your business is vulnerable</b></p>
						
						<p>If you have no plan in place, Requital Finance has one for you, and it is called probate. If you go one step further and get a will, it will still go through probate, meaning it will have to go through court. Though you can leave someone your business through your will, doing so leaves your company extremely vulnerable.<br>During probate, the court oversees your will’s administration to ensure your assets (including your business) are distributed according to your wishes. But probate can take months (or even years) to complete and can be extremely costly, seriously disrupting your operation and its cash flow. What’s more, probate is a public process, leaving your business affairs open to competitors’ prying eyes.<br>Not to mention, a will only goes into effect upon your death, so it would do nothing to protect your business should you become incapacitated by illness or injury. So, if you only have a will or no plan at all, in the event of your incapacity, your family will have to petition the court for guardianship in order to manage your business operation and other personal and financial affairs.<br>Like probate, the court process involved with guardianship can be long and costly. And in the end, whether it’s a family member or professional agency, there’s no guarantee that the individual the court ultimately names as guardian would be the best person to run your company.</p>
						<br><br>
						<p><b>A living trust offers optimal protection.</b></p>
						<p>Given the lack of protection offered by a will, one of the best ways to ensure your business’s continued success when you die or should you become incapacitated is by placing your company in a living trust. A trust is not required to go through probate, and all assets placed within the trust are immediately transferred to the person, or persons, of your choice in the event of your death or incapacity.<br>Should something happen to you, having your business held in trust would allow for the smooth transition of control of your company, without the time and expense associated with probate or guardianship. Using a trust, you can choose the individual(s) you deem best suited to run your company in your absence, whether your absence is permanent or temporary.<br>Plus, trusts are not open to the public, so your company’s internal affairs would remain private, and transfer of ownership would take place in your lawyer’s office, not a courtroom.</p>
						<br><br>
						<p><b>Comprehensive security.</b></p>
						<p>While placing your business in a living trust is an effective way to protect your business upon your death or incapacity, it’s just one of several planning tools available. And despite some common misconceptions, a trust does not provide asset protection against lawsuits or other creditor matters.</p>
						<br><br>
						<p><b>Succession Plan.</b></p>
						<p>Creating a business succession plan can help ensure your operation doesn’t fall apart when you pass on. Beyond simply naming a successor, a comprehensive succession provides stability and security by allowing you to lay out explicit instructions for how the company should be run.<br>From specifying how ownership should be transferred and providing rules for compensation to establishing dispute resolution procedures, an effective succession plan can provide the new owner with a detailed roadmap for your company’s continued success.</p>
						<br><br>
						<p><b>Buy-Sell Agreement.</b></p>
						<p>If you share ownership of your business with one or more other people, you’ll likely want to prepare a buy-sell agreement. A buy-sell agreement ensures that upon certain conditions—like your death or permanent incapacity—the remaining owners are able to purchase your shares of the business, or it can stipulate that your shares should pass to your heirs.<br>In this way, a properly drafted buy-sell agreement can prevent your family members from getting stuck owning a business they don’t want and can’t sell. And it also protects your surviving partners from being forced to deal with new owners they didn’t count on.</p>
							<br><br>
						<p><b>Life Insurance.</b></p>
						<p>Unless your business generates significant cash flow—and will continue to do so upon your death—that income might not be enough to financially provide for your loved ones. To offer a safety net for your family, team, and customers, invest in life insurance to provide liquidity while your family handles your affairs and your successor assumes control.<br>If your company has multiple owners, you can pair life insurance policies on each partner with your buy-sell agreement. That way, your remaining partners can buy out your shares at a previously agreed-upon price, and the life insurance can help pay for the buyout.</p>
							<br><br>
						<p><b>Bottom Line.</b></p>
						<p>Your business is one of your most important assets. For my restaurant-owner buddy, for myself, for my husband who owns a dental practice, and for other business owners I know, the business is like a child – you pour your time, money, and energy into raising it for success. Amidst these tumultuous times, every business owner needs to think about how to protect and preserve your most important asset for the benefit of those you love most.</p>
					
						
						
						
