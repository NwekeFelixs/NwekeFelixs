						 <p><b>Investment definition</b></p>
						 <p>An investment is a purchase that is completed with money that has the potential to produce income or a profit. Things that naturally lose value over time and with use are not investments.<br>An investor is a person or entity who outlays capital in order to produce an income or to make profits. Investing is the act of putting forth capital with the expectation of income or profit. Personal investing is buying financial securities or property for the purpose of making a profit.</p>
						 <br><br>
						 <p><b>What are the main categories of investments?</b></p>
						 <p>There are three main categories when you are considering investments. Each category breaks out into various opportunities that may suit your financial plan.<br>An equity-related investment includes stocks, options, derivatives, venture capital, index funds, and etc.<br>Low-risk investments include bonds, CDs, and savings accounts. Bonds are when you buy a debt that is expected to be repaid. Since they are considered low risk, they tend to provide a lower reward.<br>Some examples of cash or cash-equivalents investments include interest-bearing savings accounts or money market funds.<br>A portfolio is a term that refers to your particular asset collection. It might include the following types of securities:</p>
						 <br>
						 <ul>
						     <li>Stocks</li>
						     <li>Bonds</li>
						     <li>Mutual funds</li>
						     <li>Money market funds</li>
						     <li>Exchange-traded funds</li>
						 </ul>
						<p>Other define investments as including real estate, precious objects, and art. These include things that have the potential to appreciate over time.</p><br><br>
						<img src="what-is-an-investment-01.jpg" />
						<br><br>
						<p><b>American investors</b></p>
						<p>Approximately 60% of Americans own securities through their IRAs, employer-sponsored retirement plans, or taxable accounts. However, only 30% of Americans own securities in taxable accounts. According to <a href="https://www.statista.com/statistics/270034/percentage-of-us-adults-to-have-money-invested-in-the-stock-market/" target="_blank">Statista,</a> 55% of Americans are invested in the stock market.</p>
						<br /> <br /> 
				<p><b>Investment types</b></p>
						<p>There are several different investment types that you should be aware of. Stocks are securities that represent a piece of a company that you own. They are sold by the firm to a shareholder. The business then uses the money it raises to fund its operations. The shareholder receives dividends based on the company’s performance and earnings in the form of monetary compensation.<br>Common stock is a share that gives you the right to vote you at shareholders’ meetings. By contrast, preferred stock is a share that pays you predetermined dividends amounts but does not give you the right to vote at shareholders’ meetings.<br>Bonds are types of securities that are generally considered to be low-risk. They allow other entities to borrow money from you and pay you a fixed rate of interest. The government or corporation that owes you the debt repays you when your bonds mature.<br>A mutual fund contains pooled money from many people. A mutual fund’s money is invested in multiple types of securities, including stocks, bonds, money market funds, and other assets. Index funds are types of mutual funds that stick closely to preset rules. This makes it easier to track specific investments.<br>Exchange-traded funds are specific types of index funds that try to match the performance of a preselected index such as the S&P 500. Many 401(k) accounts through employers invest in different mutual funds.<br>Options are investments that are based on potential future transactions. The investors have contracts that allow them to reserve the right to trade in the assets at a later time at a specific price.<br>Retirement accounts, such as IRAs and 401(k) accounts, are tax-advantaged plans that are designed to help people prepare for their future and for retirement. The accounts can be tied to a variety of investment vehicles that heighten their value.<br>Real estate investments are purchases of tangible land or property, such as personal homes or commercial lots.<br>Money market funds have relatively little risk. By law, they invest only in specific short-term, high-quality investments issued by U.S. corporations and federal, state and local governments.<br></p>
							<img src="what-is-an-investment-01.jpg" />
							<br>
							<p>An alternative investment is a general term that is used for an investment option that is not in the stock, bond or cash category. Real estate investment trusts or REITs are public or private companies that own real property that creates income. Commodities are agricultural products or raw materials that can be bought and sold such as coffee, tea, or oil. Precious metals are metals with a high value that can be bought and sold. This includes gold, palladium, and silver.<br>Hedge funds are types of alternative investment options. They pool their investors’ money and use multiple tactics to try to provide them with returns. Private equity funds invest directly in companies. They might purchase private businesses or purchase controlling shares of publicly traded companies.</p>
					<br><br>
						<p><b>Investor</b></p>
						
						<p>Before you get started, you need to know the different types of investors. Active traders have a goal to make short-term profits through the continual buying and selling of different investment types. The benefits of active trading include that investments are adjusted to align with the prevailing market conditions for managing the investment risk. There may also be short-term opportunities that short-term traders can take advantage of.<br>Active traders tend to pay high fees because they pay fees per trade and other transactional fees. However, the fees that they pay for trading sticks are low.<br>Passive traders have a goal of building wealth over time and minimizing their buying and selling. They buy and hold securities and believe in the benefit of long-term ownership. This approach can be highly beneficial, especially when there is good diversification, though this doesn’t protect against a decline in value. The benefits of passive trading include lower costs and greater tax efficiency.<br>Since they are not constantly buying and selling, passive traders do not have to worry about paying as many commissions and management expenses. Since they hold their securities for the long-term, they generally do not have to worry about large annual capital gains taxes.<br>Passive traders may pay higher costs if they choose brokerages that charge higher management expenses and commissions. They might pay lower or no costs when they choose brokerages that charge low or no fees to invest. Semi-passive investors choose brokerages that do not charge management expenses or commissions but allow more trading activity such as do-it-yourself investing platforms. Researching online brokerages can also prove beneficial for active traders that want to take control of their investing and reduce their fees.</p>
						<br><br>
						<p><b>Return on investment (ROI)</b></p>
						<p>Your return on investment represents the financial gain or profitability percentage from an investment over a period of time. It is used in finance to compare the efficiency of different investments and is also used in conjunction with other methods of measuring your return. You can also calculate your ROI by using a <a href="https://www.calculator.net/roi-calculator.php" target="_blank">ROI calculator.</a></p>
